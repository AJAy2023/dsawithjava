public class leftside {
    public static void main(String[] args) {
        int n=4; 
        int nst=1;
        for(int i=1; i<=n; i++)
        {
            for(int k=1; k<=nst; k++)
            {
                System.out.print("*");
            }
              System.out.println();
              nst++;
        }
    }
}
